    private void calc_Size_ButttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calc_Size_ButttonActionPerformed
       // Actual Size = (Angular Size/57.3)*Distance
        double result;
        revert();
        result=(angle_Value/57.3)*distance_Value;
        String resultStr;
        resultStr = new String();
        resultStr=String.valueOf(result);
        actual_Input_Field.setText(resultStr);
    }//GEN-LAST:event_calc_Size_ButttonActionPerformed

    private void calc_Ang_Size_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calc_Ang_Size_ButtonActionPerformed
        // Angular Size=(Size/Distance)*57.3
        double result;
        String resultStr;
        resultStr = new String();
        revert();
        if (distance_Value!=0){
            result=(actual_Size/distance_Value)*57.3;
            resultStr=String.valueOf(result);
        } else {
            resultStr="Infinite";
        }
        angular_Input_Field.setText(resultStr);
    }//GEN-LAST:event_calc_Ang_Size_ButtonActionPerformed

    private void calc_Distance_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calc_Distance_ButtonActionPerformed
        // Distance=Size*(57.3/Angle)
        double result;
        String resultStr;
        resultStr = new String();
        revert();
        if (angle_Value!=0){
            result=actual_Size*(57.3/angle_Value);
            resultStr=String.valueOf(result);
        } else {
            resultStr="Infinite";
        }
        distance_Input_Field.setText(resultStr);        
    }//GEN-LAST:event_calc_Distance_ButtonActionPerformed

