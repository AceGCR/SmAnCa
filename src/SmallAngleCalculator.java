    // Reverts Text input Field back to last usable state. 
    // avoids the problems of trying to calculate with an input value of "Infinite"
    private void revert(){
       String tmpStr;
       tmpStr=new String();
       tmpStr=String.valueOf(angle_Value);
       angular_Input_Field.setText(tmpStr);
       tmpStr=String.valueOf(distance_Value);
       distance_Input_Field.setText(tmpStr);
       tmpStr=String.valueOf(actual_Size);
       actual_Input_Field.setText(tmpStr);
    }
